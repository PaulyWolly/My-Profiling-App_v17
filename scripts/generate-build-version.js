/**
 * Writes src/environments/build-version.ts from the latest git commit timestamp.
 * Runs automatically before npm start / npm run build (local + Render).
 */
const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

function getCommitDate() {
  try {
    const iso = execSync('git log -1 --format=%cI', { encoding: 'utf8', stdio: ['pipe', 'pipe', 'ignore'] }).trim();
    if (iso) {
      return new Date(iso);
    }
  } catch (_) {
    // Not a git repo or git unavailable — use build time
  }
  return new Date();
}

function formatVersionLabel(date) {
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const year = date.getFullYear();
  const hour = date.getHours();
  const minute = String(date.getMinutes()).padStart(2, '0');
  return `ver ${month}.${day}.${year}.${hour}:${minute}`;
}

const outPath = path.join(__dirname, '..', 'src', 'environments', 'build-version.ts');
const label = formatVersionLabel(getCommitDate());
const contents = `// Auto-generated by scripts/generate-build-version.js — do not edit manually
export const buildVersion = '${label}';
`;

fs.writeFileSync(outPath, contents, 'utf8');
console.log('[generate-build-version]', label);
